#include <stdio.h>

/**
 * main - prints the alphabet in lowercase, and then in uppercase,
 * followed by a new line
 * Return: Always 0 (Success)
 */
int main(void)
{
	int character;

	for (character = 'a'; ch <= 'z'; character++)
		putchar(character);
	for (ch = 'A'; ch <= 'Z'; character++)
		putchar(character);
	putchar('\n');
	return (0);
}
